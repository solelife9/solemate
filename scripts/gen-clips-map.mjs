// scripts/gen-clips-map.mjs — assets/voice/*.mp3 → lib/runVoice/clips.ts (require 정적 맵)
// RN(Metro)은 동적 require 를 못 하므로, 번들된 음성 클립을 id→require() 정적 맵으로 생성한다.
// gen-voice.mjs 로 클립을 만든 *뒤* 1회 실행: node scripts/gen-clips-map.mjs
import {readdirSync, writeFileSync} from 'node:fs';
import {join, dirname} from 'node:path';
import {fileURLToPath} from 'node:url';

const ROOT = join(dirname(fileURLToPath(import.meta.url)), '..');
const ids = readdirSync(join(ROOT, 'assets', 'voice'))
  .filter(f => f.endsWith('.mp3'))
  .map(f => f.replace(/\.mp3$/, ''))
  .sort();

const lines = ids.map(id => `  '${id}': require('../../assets/voice/${id}.mp3'),`).join('\n');
const out = `// AUTO-GENERATED by scripts/gen-clips-map.mjs — 직접 수정 금지.
// 러닝 음성 코칭 클립(assets/voice/*.mp3)의 id→번들 require 정적 맵. ${ids.length}개.
export const CLIPS: Record<string, number> = {
${lines}
};
`;
writeFileSync(join(ROOT, 'lib', 'runVoice', 'clips.ts'), out);
console.log(`clips.ts 생성: ${ids.length}개`);
